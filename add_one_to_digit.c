/*
If  a  number  is  input  through  the  keyboard,  write  a  program  to  print  a  new  number  by  adding  one  to  each  of  its  digits. 
*/

#include <stdio.h>

int main()
{
	int num;
	
	printf("Enter a five digit number to add one to each digit ");
	scanf("%d",&num);

	printf("The number after adding one to each digit %d\n",num+11111);
}
